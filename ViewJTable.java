    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

//        System.out.println("Linha selecionada: "+jTProdutos.getSelectedRow());
        if (jTClientes.getSelectedRow() != -1) {

            Clientes c = new Clientes();
            ClientesDAO dao = new ClientesDAO();

            c.setId((int) jTClientes.getValueAt(jTClientes.getSelectedRow(), 0));

            dao.delete(c);

            txtNome.setText("");
            txtTel.setText("");
            txtEnd.setText("");
            txtPedi.setText("");
            txtData.setText("");

            readJTable();

        } else {
            JOptionPane.showMessageDialog(null, "Selecione um produto para excluir.");
        }


    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:

        if (jTClientes.getSelectedRow() != -1) {

            Clientes c = new Clientes();
            ClientesDAO dao = new ClientesDAO();

            c.setNome(txtNome.getText());
            c.setTelefone(txtTel.getText());
            c.setEndereco(txtEnd.getText());
            c.setPedido(txtPedi.getText());
            c.setData_entrega(txtData.getText());
            c.setId((int) jTClientes.getValueAt(jTClientes.getSelectedRow(), 0));
            dao.update(c);

            txtNome.setText("");
            txtTel.setText("");
            txtEnd.setText("");
            txtPedi.setText("");
            txtData.setText("");

            readJTable();

        }


    }//GEN-LAST:event_jButton3ActionPerformed

